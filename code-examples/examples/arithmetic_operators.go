package examples

import "fmt"

func Operators() {
	// sum
	x := 10
	y := 50
	result := x + y
	fmt.Println(result)

	// subtraction
	result = y - x
	fmt.Println(result)

	// multiplication
	result = x * y
	fmt.Println(result)

	// division
	result = y / 10
	fmt.Println(result)

	// module
	result = y % x
	fmt.Println(result)

	// increase
	x++

	// decrease
	x--

	// Println
	// automatic car return
	fmt.Println(result)
}
